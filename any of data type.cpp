#include <iostream>

int main() {
   
    int integerVariable = 42;
    std::cout << "Integer Variable: " << integerVariable << std::endl;

   
    float floatVariable = 3.14;
    std::cout << "Float Variable: " << floatVariable << std::endl;

    
    char charVariable = 'A';
    std::cout << "Character Variable: " << charVariable << std::endl;

    return 0;
}

